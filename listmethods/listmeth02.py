# Create a list of numbers
numbers = [1, 2, 3, 4, 5]

# Make a copy of the list using the copy() method
numbers_copy = numbers.copy()

# Print the original list and the copy
print("Original list:", numbers)
print("Copy of list:", numbers_copy)

# Append a new number to the original list
numbers.append(6)

# Print both lists again to see the difference
print("Original list after append:", numbers)
print("Copy of list after append:", numbers_copy)
